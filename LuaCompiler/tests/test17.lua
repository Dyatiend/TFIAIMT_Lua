operaion = read()
n1 = read()
n2 = read()

if operaion == "+" then
    print(n1 + n2)
elseif operaion == "-" then
    print(n1 - n2)
elseif operaion == "*" then
    print(n1 * n2)
elseif operaion == "//" then
    print(n1 // n2)
elseif operaion == "%" then
    print(n1 % n2)
elseif operaion == ".." then
    print(n1 .. n2)
elseif operaion == "^" then
    print(n1 ^ n2)
end